package main

import (
	"fmt"
	"math"
)

func main() {

	fmt.Println("chantal" + "rain")
	// strings can be added together using +

	fmt.Println("1+1 =", 1+1)
	res_1 := math.Round(7.0 / 3.0)
	fmt.Println("The rounded answer to the equation of 7.0/3.0 is =", res_1)
	// strings and integers

	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	// boolean and boolean operators
}
